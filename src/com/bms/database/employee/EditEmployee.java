    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (txtName.getText().isEmpty()) {
            txtName.setForeground(Color.decode(Utils.settings.getColorCode()));
            txtName.setText("Name required");
            errors += 1;
        } else {
            txtName.setForeground(Color.BLACK);
        }

        if (txtMobile.getText().length() != 11 || !txtMobile.getText().startsWith("09")) {
            txtMobile.setForeground(Color.decode(Utils.settings.getColorCode()));
            txtMobile.setText("Mobile number is invalid");
            errors += 1;
        } else {
            txtMobile.setForeground(Color.BLACK);
        }

        if (txtPasswordHint.getText().isEmpty()) {
            txtPasswordHint.setForeground(Color.decode(Utils.settings.getColorCode()));
            txtPasswordHint.setText("Password hint required");
            errors += 1;
        } else {
            txtPasswordHint.setForeground(Color.BLACK);
        }

        if (errors == 0) {
            try {
                employee.setName(txtName.getText());
                employee.setMobile(txtMobile.getText());
                employee.setNcode(txtNcode.getText());
                employee.setUser(Security.encrypt(Utils.envs.get("encKey"), txtNcode.getText()));
                employee.setHint(txtPasswordHint.getText());
            } catch (Exception e) {
                Utils.swingUI.showErrorDialog(e.getMessage(), "Employee values failed.");
            }
            
            if (employeeRepository.editEmployee(employee)) {
                Utils.envs.put("tableUpdate", "3");

                Utils.swingUI.showInformationDialog("The employee information in database updated successfully.",
                        "Employee updated successfully.");
            }
        } else {
            errors = 0;
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

