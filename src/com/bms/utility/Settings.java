package com.bms.utility;

public class Settings {

    private String tableRecords;
    private String closeRegister;
    private String dialogPath;
    private String colorCode;
    private String payment;
    private String defaultMoney;

    public String getTableRecords() {
        return tableRecords;
    }

    public void setTableRecords(String tableRecords) {
        this.tableRecords = tableRecords;
    }

    public String getCloseRegister() {
        return closeRegister;
    }

    public void setCloseRegister(String closeRegister) {
        this.closeRegister = closeRegister;
    }

    public String getDialogPath() {
        return dialogPath;
    }

    public void setDialogPath(String dialogPath) {
        this.dialogPath = dialogPath;
    }

    public String getColorCode() {
        return colorCode;
    }

    public void setColorCode(String colorCode) {
        this.colorCode = colorCode;
    }

    public String getPayment() {
        return payment;
    }

    public void setPayment(String payment) {
        this.payment = payment;
    }

    public String getDefaultMoney() {
        return defaultMoney;
    }

    public void setDefaultMoney(String defaultMoney) {
        this.defaultMoney = defaultMoney;
    }
    
    
}
